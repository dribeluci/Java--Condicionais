/* Exercício proposto:
Faça um programa em que permita a entrada de um número qualquer
e exiba se este número é par ou ímpar. Se for par exiba também a 
raiz quadrada do mesmo; se for ímpar exiba o número elevado ao quadrado*/


package br.com.exercicioscondicionais;

import java.util.Scanner;

	public class parImpar {
	
	public static void main(String[] args) {
		
		
		Scanner entrada = new Scanner(System.in);
				
		int numero; 
		
		System.out.println("Digite um número: ");
		numero= entrada.nextInt();

		
		if (numero % 2 == 0) { 
			 System.out.println(" O número " + numero + " é par!\nO resultado da sua Raiz quadrada é:  " +  Math.sqrt(numero));
		}
		
		else {
			System.out.println("O número " + numero +  " é impar! \nO resultado da sua potência é: " + Math.pow(numero,2));
		}
		
		entrada.close();
	}
}